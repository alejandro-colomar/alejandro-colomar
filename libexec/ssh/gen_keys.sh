################################################################################
##	definitions							      ##
################################################################################
ARGC=0;


################################################################################
##	functions							      ##
################################################################################


################################################################################
##	main								      ##
################################################################################
function main()
{
	for remote in ${all_machines}; do
		echo "	SSH	${remote}"
		sshpass -e \
		ssh ${ssh_opts} ${remote} "
			echo \"	SSH-KEYGEN\";
			ssh-keygen -t rsa -b 4096 -f ~/.ssh/id_rsa -P '' ||:;
		";
	done
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo	"Illegal number of parameters (Requires ${ARGC})";
	exit	${EX_USAGE};
fi

main;


