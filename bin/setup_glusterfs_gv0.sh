################################################################################
##	definitions							      ##
################################################################################
ARGC=0;


################################################################################
##	functions							      ##
################################################################################


################################################################################
##	main								      ##
################################################################################
function main()
{
	for remote in ${workers[*]}; do
		echo "	SSH	${remote}";
		ssh ${remote} "
			sudo install /usr/local/src/server/etc/fstab /etc/;
			sudo mount -a;
		";
	done
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo	"Illegal number of parameters (Requires ${ARGC})";
	exit	${EX_USAGE};
fi

main;


